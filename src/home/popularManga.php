<script>
	var slideIndex = 1;
	var idInterval = null;
	showDivs(slideIndex);
	resetInterval();

    function resetInterval(){
    	if(idInterval !== null) {
    		clearInterval(idInterval);
    		idInterval = null;
    	}
    	if(idInterval === null) {
    		idInterval = setInterval(function() {
		        plusDivs(1);
		    }, 5000);
    	}
    }

	function plusDivs(n) {
		slideIndex = slideIndex+n;
		if(n < 0) {
			showDivs(slideIndex, 'left');
		} else {
			showDivs(slideIndex);
		}
		resetInterval();
	}

	function currentDiv(n) {
	  	showDivs(slideIndex = n);
	  	resetInterval();
	}

	const content = document.getElementById("content");
	content.addEventListener("mouseenter", function() {
		clearInterval(idInterval);
		idInterval = null;
	});
	content.addEventListener("mouseleave", function() {
		if(idInterval === null) {
			idInterval = setInterval(function() {
			        plusDivs(1);
			    }, 5000);
		}
	});

	function showDivs(n, animate=null) {
	    var i;
	    var x = document.getElementsByClassName("mySlides");
	    if (n > x.length) {slideIndex = 1}
	    if (n < 1) {slideIndex = x.length}
	    for (i = 0; i < x.length; i++) {
	    	x[i].style.display = "none";
	    	x[i].className = x[i].className.replace(" w3-animate-left", "");
	    	x[i].className = x[i].className.replace(" w3-animate-right", "");
	    }
	    if (animate === null) {
	  		x[slideIndex-1].className += " w3-animate-right";
	    } else {
	  		x[slideIndex-1].className += " w3-animate-left";
	    }
	    x[slideIndex-1].style.display = "block";
	}
</script>